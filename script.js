let slideIndex = 1;

function openModal(wonderKey) {
    const modal = document.getElementById('wonder-modal');
    const title = document.getElementById('modal-title');
    const desc = document.getElementById('modal-desc');
    const sliderContainer = document.getElementById('slider-container');
    
    const data = wondersData[wonderKey];
    
    title.innerText = data.title;
    desc.innerText = data.description;

    let slidesHTML = '';
    
    data.images.forEach(imgUrl => {
        slidesHTML += `
            <div class="slide fade">
                <img src="${imgUrl}" alt="Slide" onerror="this.style.display='none'; this.parentElement.innerHTML='<div style=\'height:100%;background:#333;color:#fff;display:flex;align-items:center;justify-content:center;\'>Image not found: ${imgUrl}</div>'">
            </div>
        `;
    });
    slidesHTML += `<a class="prev" onclick="changeSlide(-1)">&#10094;</a>`;
    slidesHTML += `<a class="next" onclick="changeSlide(1)">&#10095;</a>`;

    sliderContainer.innerHTML = slidesHTML;

    modal.style.display = "block";
    
    slideIndex = 1;
    showSlides(slideIndex);
}

function closeModal() {
    document.getElementById('wonder-modal').style.display = "none";
}

window.onclick = function(event) {
    const modal = document.getElementById('wonder-modal');
    if (event.target == modal) {
        modal.style.display = "none";
    }
}

function changeSlide(n) {
    showSlides(slideIndex += n);
}

function showSlides(n) {
    let i;
    let slides = document.getElementsByClassName("slide");
    if (!slides.length) return;

    if (n > slides.length) {slideIndex = 1}
    if (n < 1) {slideIndex = slides.length}
    
    for (i = 0; i < slides.length; i++) {
        slides[i].style.display = "none";
    }
    slides[slideIndex-1].style.display = "block";
}
